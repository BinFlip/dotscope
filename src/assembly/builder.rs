    /// Load argument by index (short form).
    ///
    /// Load an argument by its index using the short form instruction, which
    /// uses a single byte for the index (0-127 due to signed byte encoding).
    ///
    /// **Opcode**: `0x0E`  
    /// **Stack**: `... → ..., argN`
    ///
    /// # Parameters
    ///
    /// * `index` - Argument index (0-127)
    ///
    /// # Examples
    ///
    /// ```rust,no_run
