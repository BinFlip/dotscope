    /// # Ok::<(), dotscope::Error>(())
    /// ```
    pub fn ldarg_s(&mut self, index: i8) -> Result<&mut Self> {
        self.encoder
            .emit_instruction("ldarg.s", Some(Operand::Immediate(Immediate::Int8(index))))?;
        Ok(self)
    }

