        asm.ldarg_0()?
            .ldc_i4_0()?
            .bgt_s("positive")?
            .ldc_i4_0()?
            .ret()?
            .label("positive")?
            .ldc_i4_1()?
            .ret()?;

        let bytecode = asm.finish()?;
